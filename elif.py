numero=int(input("Cual es el número que deseas convertir?: "))

if numero==1:
    print("En número es 'uno'")
    
elif numero==2:
    print("El número es 'dos'")
elif numero==3:
    print("El número es 'tres'")
elif numero==4:
    print("El número es 'cuatro'")
elif numero==5:
    print("El número es 'cinco'")
    
else:
    print("Este programa solo puede converitir hasta el numero 5")

print("Fin")
